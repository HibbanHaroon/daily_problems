def countApplesAndOranges(s, t, a, b, apples, oranges):
    count_of_apples = 0
    count_of_oranges = 0

    for apple in apples:
        total_distance_of_apple = a + apple
        if total_distance_of_apple >= s and total_distance_of_apple <= t:
            count_of_apples += 1

    for orange in oranges:
        total_distance_of_orange = b + orange
        if total_distance_of_orange >= s and total_distance_of_orange <= t:
            count_of_oranges += 1

    print(count_of_apples)
    print(count_of_oranges)


